    public function findByContact(Contact $contact): array
    {
        return $this->createQueryBuilder('c')
            ->innerJoin('c.contacts', 'contact')
            ->where('contact.id = :contactId')
            ->setParameter('contactId', $contact->getId())
            ->getQuery()
            ->getResult();
    }


